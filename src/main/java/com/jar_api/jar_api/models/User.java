package com.jar_api.jar_api.models;

import jakarta.persistence.*;

// so I believe this class acts as the mapping for a db query for a user from a users table
// and turns it into a User java object which gets created based on properties from the users table

// this implementation taken from:
// https://www.twilio.com/en-us/blog/beginner-mysql-database-java-spring-boot#Create-the-model-class

@Entity
@Table(name = "users")
public class User {
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Column(name = "user_id")
    private Long id;

    @Column(name = "username")
    private String username;

    @Column(name = "email")
    private String email;

    @Column(name = "display_name")
    private String displayName;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getDisplayName() {
        return displayName;
    }

    public void setDisplayName(String displayName) {
        this.displayName = displayName;
    }

}
